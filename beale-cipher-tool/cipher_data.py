def get_cipher_stats(cipher_sequence):
    """
    Return statistical information about a cipher sequence.

    Args:
        cipher_sequence: List of integers representing the cipher

    Returns:
        dict: Statistics including max, min, length, and required key length
    """
    return {
        "length": len(cipher_sequence),
        "max_value": max(cipher_sequence),
        "min_value": min(cipher_sequence),
        "required_key_length": max(cipher_sequence)
        + 10,  # Add buffer for offset testing
        "avg_value": sum(cipher_sequence) / len(cipher_sequence),
    }


