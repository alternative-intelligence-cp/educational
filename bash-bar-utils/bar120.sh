BAR120_IS_VALID_REPS(){
	local argument="$1" # Store the first argument
	# Regular expression for a signed or unsigned integer
	# ^    - start of the string
	# -?   - optional negative sign
	# [0-9]+ - one or more digits
	# $    - end of the string
	if [[ "$argument" =~ ^-?[0-9]+$ ]]; then
		if [[ $argument -gt 0 ]]; then
    		echo "true"
		else
			echo "false"
		fi
	else
    	echo "false"
	fi
}
BAR120_REPEAT(){
	local char="$1"
	local reps="$2"
	local str=""
	if [ -z char ]; then
		char="*"
	fi
	if [ $(BAR120_IS_VALID_REPS $reps) == "false" ]; then
		reps=120
	fi
	for((i=0;i<reps;i++)); do
		str=$str$char
	done
	echo "$str"
}
BAR120_CENTER(){
	local spc="$BAR120_SPCE"
	local wid=120;
	local text="$1"
	local char="$2"
	local len=
	local left=
	local pad=
	local pdm=
	local str=
	local modu=
	if [ -z text ]; then
		text="$spc"
	fi
	if [ -z char ]; then
		char="$spc"
	fi
	len=${#text}
	left=$(( $wid - $len ));
	pad=$(( $left / 2 ));
	pdm=$(( $pad - 1 ));
	str="$char"
	for ((i=1;i<=pdm;i++)); do
    		str="$str$spc"
	done
	str="$str$text"
	for ((i=1;i<=pdm;i++)); do
    		str="$str$spc"
	done
	modu=$(( $left % 2 ))
	if [ $modu -gt 0 ]; then
		str="$str$spc"
	fi
	str="$str$char"
	echo "$str"
}

BAR120_BOX(){
	local spc=$BAR120_SPCE
	local text="$1"
	local style="$2"
	local top=
	local cnt=
	local btm=
	local ends=
	case "$style" in
		ASTR | astr)
			top="$BAR120_ASTR"
			ends='*'
			btm="$BAR120_ASTR"
			;;
		HASH | hash)
			top="$BAR120_HASH"
			ends='#'
			btm="$BAR120_HASH"
			;;
		default | *)
			top="$BAR120_TPBT"
			ends="$BAR120_ENDB"
			btm="$BAR120_TPBT"
			;;
	esac
	cnt="$(BAR120_CENTER "$text" "$ends")"
	echo -e "$top\n$cnt\n$btm"
}





