BAR80_IS_VALID_REPS(){
	local argument="$1" # Store the first argument
	# Regular expression for a signed or unsigned integer
	# ^    - start of the string
	# -?   - optional negative sign
	# [0-9]+ - one or more digits
	# $    - end of the string
	if [[ "$argument" =~ ^-?[0-9]+$ ]]; then
		if [[ $argument -gt 0 ]]; then
    		echo "true"
		else
			echo "false"
		fi
	else
    	echo "false"
	fi
}
BAR80_REPEAT(){
	local char="$1"
	local reps="$2"
	local str=""
	if [ -z char ]; then
		char="*"
	fi
	if [ $(BAR80_IS_VALID_REPS $reps) == "false" ]; then
		reps=80
	fi
	for((i=0;i<reps;i++)); do
		str=$str$char
	done
	echo "$str"
}
BAR80_CENTER(){
	local spc="$BAR80_SPCE"
	local wid=80;
	local text="$1"
	local char="$2"
	local len=
	local left=
	local pad=
	local pdm=
	local str=
	local modu=
	if [ -z text ]; then
		text="$spc"
	fi
	if [ -z char ]; then
		char="$spc"
	fi
	len=${#text}
	left=$(( $wid - $len ));
	pad=$(( $left / 2 ));
	pdm=$(( $pad - 1 ));
	str="$char"
	for ((i=1;i<=pdm;i++)); do
    		str="$str$spc"
	done
	str="$str$text"
	for ((i=1;i<=pdm;i++)); do
    		str="$str$spc"
	done
	modu=$(( $left % 2 ))
	if [ $modu -gt 0 ]; then
		str="$str$spc"
	fi
	str="$str$char"
	echo "$str"
}

BAR80_BOX(){
	local spc=$BAR80_SPCE
	local text="$1"
	local style="$2"
	local top=
	local cnt=
	local btm=
	local ends=
	case "$style" in
		ASTR | astr)
			top="$BAR80_ASTR"
			ends='*'
			btm="$BAR80_ASTR"
			;;
		HASH | hash)
			top="$BAR80_HASH"
			ends='#'
			btm="$BAR80_HASH"
			;;
		default | *)
			top="$BAR80_TPBT"
			ends="$BAR80_ENDB"
			btm="$BAR80_TPBT"
			;;
	esac
	cnt="$(BAR80_CENTER "$text" "$ends")"
	echo -e "$top\n$cnt\n$btm"
}





